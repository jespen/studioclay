export function createTransporter() {
  console.log('üìß TRANSPORTER DIAGNOSTIC 1: Function createTransporter called');
  
  // Log process environment information for debugging
  const processInfo = {
    nodeEnv: process.env.NODE_ENV,
    vercelEnv: process.env.VERCEL_ENV,
    region: process.env.VERCEL_REGION,
    isProduction: process.env.NODE_ENV === 'production'
  };
  
  console.log('üìß TRANSPORTER DIAGNOSTIC 2: Process info:', processInfo);
  
  // Get email configuration from environment variables
  const host = process.env.EMAIL_SMTP_HOST || 'smtp.office365.com';
  const port = parseInt(process.env.EMAIL_SMTP_PORT || '587', 10);
  const secure = process.env.EMAIL_SMTP_SECURE === 'true';
  const user = process.env.EMAIL_USER;
  const pass = process.env.EMAIL_PASS;
  const defaultFrom = process.env.EMAIL_FROM || 'eva@studioclay.se';
  const bcc = process.env.EMAIL_BCC;
  
  console.log('üìß =========== EMAIL CONFIGURATION DETAILS ===========');
  console.log(`üìß Running in environment: ${process.env.NODE_ENV}`);
  console.log(`üìß VERCEL_ENV: ${process.env.VERCEL_ENV}`);
  console.log(`üìß SMTP Host: ${host}`);
  console.log(`üìß SMTP Port: ${port}`);
  console.log(`üìß SMTP Secure: ${secure}`);
  console.log(`üìß Email User: ${user}`);
  console.log(`üìß Email Pass: ${pass ? '[set]' : '[not set]'}`);
  console.log(`üìß Email Pass Length: ${pass ? pass.length : 0}`);
  console.log(`üìß Studio Clay Email: default: ${defaultFrom}`);
  console.log(`üìß BCC Email: ${bcc ? bcc : '[not set]'}`);
  console.log(`üìß DISABLE_ETHEREAL: default: ${process.env.DISABLE_ETHEREAL}`);
  console.log('üìß ================================================');
  
  if (!user || !pass) {
    console.error('üìß ERROR: Missing email credentials - user or password not set');
    console.error(`üìß EMAIL_USER: ${user ? 'set' : 'missing'}`);
    console.error(`üìß EMAIL_PASS: ${pass ? 'set' : 'missing'}`);
    throw new Error('Email credentials are missing. Please check environment variables.');
  }
  
  // In production/preview we use the real SMTP server
  // In development we can use Ethereal for testing unless explicitly disabled
  const isDevMode = process.env.NODE_ENV === 'development' && process.env.DISABLE_ETHEREAL !== 'true';
  
  if (isDevMode) {
    console.log('üìß Using Ethereal for email testing (development mode)');
    // For development, we'll create an Ethereal test account in the actual send function
    return null;
  } else {
    console.log('üìß Using real SMTP server for email delivery (production mode)');
    console.log(`üìß SMTP Configuration: ${host}:${port} (secure: ${secure})`);
    console.log(`üìß Auth User: ${user}`);
    
    // For production, create an actual SMTP transport
    // When secure is false, we'll use STARTTLS which happens after connecting
    if (!secure) {
      console.log('üìß Creating SMTP transport with TLS');
    } else {
      console.log('üìß Creating direct secure SMTP transport (SSL/TLS)');
    }
    
    const transporter = nodemailer.createTransport({
      host,
      port,
      secure, // true for 465, false for 587 (STARTTLS)
      auth: {
        user,
        pass
      },
      debug: true, // Enable verbose logging
      logger: true, // Log SMTP traffic
      connectionTimeout: 30000, // 30 seconds connection timeout 
      greetingTimeout: 30000, // 30 seconds greeting timeout
      socketTimeout: 60000, // 60 seconds socket timeout
    });
    
    // Verify the connection configuration
    // We're doing this here so we can catch configuration errors early
    console.log('üìß Verifying SMTP connection...');
    
    return transporter;
  }
}

